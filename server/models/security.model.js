import mongoose from "mongoose";

const SecuritySchema = new mongoose.Schema({
    name: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
        unique: true,
    },
    password: {
        type: String,
        required: true,
    },
});

export const SecurityModel = mongoose.model("Security", SecuritySchema);
